#!/usr/bin/env bash
# gen-screen-tapes.sh — emit one VHS .tape per TUI screen + run them all.
# Each tape produces a GIF under tapes/out/screens/<view>.gif so the visual
# state of every screen lives alongside the snapshot goldens.
set -euo pipefail

cd "$(dirname "$0")/.."

views=(dashboard licenses issuers recipients identities revocation servers totp audit settings)

mkdir -p tapes/screens tapes/out/screens

for v in "${views[@]}"; do
  seed_arg=""
  if [ -f "scripts/tui-snap-seeds/${v}.json" ]; then
    seed_arg=" -seed scripts/tui-snap-seeds/${v}.json"
  fi
  cat > "tapes/screens/${v}.tape" <<TAPE
# Auto-generated by scripts/gen-screen-tapes.sh. Re-run the script to refresh.
Output tapes/out/screens/${v}.gif

Require vhs
Set Shell "bash"
Set Width 1280
Set Height 720
Set FontSize 14
Set Theme "Catppuccin Mocha"

Hide
Type "make tui-snap >/dev/null 2>&1"
Enter
Show

Type "clear; bin/tui-snap -view ${v}${seed_arg}"
Enter
Sleep 1500ms
TAPE
done

# Run each tape — vhs takes ~3-5s per tape, so the full sweep is ~40s.
for v in "${views[@]}"; do
  echo "==> vhs tapes/screens/${v}.tape"
  vhs "tapes/screens/${v}.tape" >/dev/null
done

echo
echo "Generated: tapes/screens/*.tape (${#views[@]} files)"
echo "Rendered:  tapes/out/screens/*.gif"
ls -lh tapes/out/screens/ 2>/dev/null | tail -n +2 | awk '{printf "  %-30s %6s\n", $9, $5}'
